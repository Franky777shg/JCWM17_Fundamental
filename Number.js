// Arithmetic Numbers
let a = 1 + 2 // penambahan
console.log(a)
let b = 4 - 1 // pengurangan
console.log(b)
let c = 2 * 3 // perkalian
console.log(c)
let d = 6 / 2 // pembagian
console.log(d)
let e = 7 % 2 // modulus => 7 - 2 - 2 - 2
console.log(e)
let f = 2
f++ // increment
console.log(f)
let g = 2
g-- // decrement
console.log(g)

// shorthand operators
let h = 2
h = h + 5
console.log(h)

let i = 2
i += 5 // i = i + 5
console.log(i)

let j = 3
j %= 2 // j = j % 2
console.log(j)

// Math Object
console.log(Math.PI) // PI
console.log(Math.abs(2)) // membuat sebuah data menjadi positif
console.log(Math.pow(8, 2)) // pangkat
console.log(Math.sqrt(64)) // akar
console.log(Math.cbrt(27)) // akar 3
console.log(Math.round(2.4)) // Math.round => pembulatan ke integer terdeket
console.log(Math.round(2.7))
console.log(Math.ceil(2.1)) // pembulatan ke atas
console.log(Math.floor(2.7)) // pembulatan ke bawah
console.log(Math.min(1, 3, 5)) // mencari data terendah
console.log(Math.max(1, 3, 5)) // mencari data tertinggi
console.log(Math.random()) // menghasilkan angka random dari 0 sampai 1

