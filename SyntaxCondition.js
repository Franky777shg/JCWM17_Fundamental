// IF
let age = 18
if (age >= 18) console.log('SIM akan segera dibuat')

// IF ELSE
if (age >= 18) {
    console.log('SIM akan segera dibuat')
} else {
    console.log('Permohonan SIM ditolak')
}

// IF ELSE IF ELSE
